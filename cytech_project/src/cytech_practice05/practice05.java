package cytech_practice05;

public class practice05 {
	public static void main(String[] args) {
		//変数の定義
		int cytech1 = 10;
		int cytech2 = 50;
		
		//演算子を使用した計算
		System.out.println("cytech1+cytech2は" + (cytech1 + cytech2) + "です。");
		System.out.println("cytech1-cytech2は" + (cytech1 - cytech2) + "です。");
		System.out.println("cytech1×cytech2は" + (cytech1 * cytech2) + "です。");
		System.out.println("cytech1÷cytech2は" + (cytech1 / cytech2) + "です。");
		System.out.println("cytech1cytech2は" + (cytech1 % cytech2) + "です。");
	}
}
