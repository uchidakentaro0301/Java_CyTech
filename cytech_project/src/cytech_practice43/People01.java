package cytech_practice43;

public class People01 {

	/**名前 */
	private String name;
	
	/**性別 */
	private String gender;
	
	/*
	 * 名前を取得（getter）
	 * @return 名前
	 */
	public String getName() {
		return name;
	}
	
	/*
	 * 性別を取得（getter）
	 * @return 性別
	 */
	public String getGender() {
		return gender;
	}
	
	/*
	 * 名前を設定（setter）
	 * @param 名前
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/*
	 * 性別を設定（setter）
	 * @param 性別
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}
}


