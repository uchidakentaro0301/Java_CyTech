package cytech_practice09;

public class practice09 {

	public static void main(String[] args) {
		
		System.out.println("200*10は" + 200 * 10 + "です。");
		System.out.println("200+10は" + 200 + 10 + "です。");
		
		//()を追加
		System.out.println("200*10は" + (200 * 10) + "です。");
		System.out.println("200+10は" + (200 + 10) + "です。");
	}
}

